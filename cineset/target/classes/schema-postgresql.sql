DROP TABLE IF EXISTS Usuario;
DROP TABLE IF EXISTS Filmes;
DROP TABLE IF EXISTS Reviews;
DROP TABLE IF EXISTS Like_filmes;

/* TABLE USUARIO */

CREATE TABLE
IF NOT EXISTS Usuario
(
ID SERIAL PRIMARY KEY,
NOMEUSUARIO VARCHAR NOT NULL
(100),
EMAIL VARCHAR
(50),
SENHA VARCHAR
(10),
GENERO VARCHAR
(15)

);

/* TABLE FILMES */

CREATE TABLE
IF NOT EXISTS Filmes
(
    IDFILME SERIAL PRIMARY KEY,
    TITULO VARCHAR NOT NULL
(100),
    GENEROFILME VARCHAR
(50),
    ANO INT,
    DIRETOR VARCHAR
(50),
    IMAGEM_LINK TEXT,
    SINOPSE TEXT NOT NULL,


);

/* TABLE REVIEWS */

CREATE TABLE
IF NOT EXISTS Reviews
(
    IDREVIEW SERIAL PRIMARY KEY,
    REVIEW TEXT NOT NULL,
    NOTA INT,
    CONSTRAINT FK_USUARIO
        FOREIGN KEY
(ID_USUARIO)
            REFERENCES Usuario
(ID)
    
);


/* TABLE LIKE_FILMES */

CREATE TABLE
IF NOT EXISTS Like_filmes
(
    ID_FILME INT,
    ID_USUARIO INT,
    PRIMARY KEY
(ID_FILME, ID_USUARIO),
)
