DROP TABLE IF EXISTS Usuario;
DROP TABLE IF EXISTS Filmes;
DROP TABLE IF EXISTS Reviews;
DROP TABLE IF EXISTS Like_filmes;

/* TABLE USUARIO */

CREATE TABLE
IF NOT EXISTS Usuario
(
ID SERIAL PRIMARY KEY,
NOMEUSUARIO VARCHAR
(100),
EMAIL VARCHAR
(50),
SENHA VARCHAR
(10)
);

/* TABLE FILMES */

CREATE TABLE
IF NOT EXISTS Filmes
(
    IDFILME SERIAL PRIMARY KEY,
    TITULO VARCHAR
(100),
    GENEROFILME VARCHAR
(50),
    BIRTHYEAR VARCHAR
(4),
    DIRETOR VARCHAR
(50),
    SINOPSE VARCHAR
(500)
);

CREATE TABLE
IF NOT EXISTS Reviews
(
    IDREVIEW SERIAL PRIMARY KEY,
    REVIEW  VARCHAR(1000)
);

CREATE TABLE
IF NOT EXISTS Like_filmes
(
    ID_FILME INT,
    ID_USUARIO INT,
    PRIMARY KEY
(ID_FILME, ID_USUARIO)
)

